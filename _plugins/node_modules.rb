# frozen_string_literal: true

module Jekyll
  module Tags
    # Crea un tag de Liquid que busca un archivo dentro del directorio
    # node_modules y lo incorpora tal cual
    class NodeModules < Liquid::Tag
      def initialize(tag, markup, tokens)
        super

        @file = File.join('node_modules', markup.strip)
      end

      def render(_context)
        File.read @file
      end
    end
  end
end

# Registrar el tag
Liquid::Template.register_tag('node_module', Jekyll::Tags::NodeModules)
